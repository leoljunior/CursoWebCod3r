const [a, b, c, d] = [3, 5, 1, 15]

const soma = a + b + c + d
const subtracao = d - b
const multiplicacao = a * b
const divisao = d / a
const modulo = a % 2


console.log(`Resultado da soma de ${a} + ${b} + ${c} + ${d} = ${soma}`)
console.log(`Resultado da subtração de ${d} - ${b} = ${subtracao}`)
console.log(`Resultado da multiplicação de ${a} * ${b} = ${multiplicacao}`)
console.log(`Resultado da divisão de ${d} / ${a} = ${divisao}`)
console.log(`Resultado de modulo de ${a} % 2 = ${modulo}`)